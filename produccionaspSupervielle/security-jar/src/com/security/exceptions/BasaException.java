package com.security.exceptions;

public class BasaException extends Exception {
	
	private static final long serialVersionUID = 1123L;

	public BasaException() {
		super("Ha ocurrido un error en la aplicacion");
	}

	public BasaException(String message, Throwable cause) {
		super(message, cause);
	}

	public BasaException(String message) {
		super(message);
	}

	public BasaException(Throwable cause) {
		super("Ha ocurrido un error en la aplicacion",cause);
	}
}
