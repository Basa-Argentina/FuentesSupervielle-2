	@Override
	public List<FacturaDetalle> listarFacturaDetallePorFactura(Factura factura, ClienteAsp cliente){
		Session session = null;
        try {
        	//obtenemos una sesión
			session = getSession();
        	Criteria crit = session.createCriteria(getClaseModelo());
        	crit.createCriteria("factura", "factura");
        	if(factura!=null){
        		crit.add(Restrictions.eq("factura", factura));
        	}
        	if(cliente != null){
        		crit.add(Restrictions.eq("factura.clienteAsp", cliente));
        	}
        	crit.setResultTransformer(Criteria.DISTINCT_ROOT_ENTITY);
        	crit.addOrder(Order.asc("orden"));
            return crit.list();
        } catch (HibernateException hibernateException) {
        	logger.error("No se pudo listar ", hibernateException);
	        return null;
        }finally{
        	try{
        		session.close();
        	}catch(Exception e){
        		logger.error("No se pudo cerrar la sesión", e);
        	}
        }
    }
	
